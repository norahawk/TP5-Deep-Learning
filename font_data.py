def _hex_row_to_bits(value, width=5):
    """Convert a 5-bit integer to a list of bits, MSB first."""
    return [(value >> (width - 1 - i)) & 1 for i in range(width)]


def _parse_font(font_raw, width=5, height=7):
    """Return float64 array of shape (32, 35) from raw hex data."""
    patterns = []
    for char_rows in font_raw:
        rows = [_hex_row_to_bits(v, width) for v in char_rows]
        patterns.append(np.array(rows).flatten())
    return np.array(patterns, dtype=np.float64)


